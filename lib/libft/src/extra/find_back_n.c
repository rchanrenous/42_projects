#include "libft.h"

int	find_back_n(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (-1);
	while (s[i] && s[i] != '\n')
		i++;
	if (s[i] && s[i] == '\n')
		return (i);
	return (-1);
}
